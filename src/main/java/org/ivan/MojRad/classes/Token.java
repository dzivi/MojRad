package org.ivan.MojRad.classes;

public class Token {
	String token;
	String ID;
	
	public Token (){
		
	}

	public Token(String token, String iD) {
		super();
		this.token = token;
		this.ID = iD;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}

	

	
	

}
